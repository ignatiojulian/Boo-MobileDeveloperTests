class Profile {
  final String name;
  final int age;
  final String role;
  final String location;
  final bool verified;
  final String mbti;
  final String sign;
  final String bio;
  final List<String> images;
  final List<String> interests;
  const Profile({
    required this.name,
    required this.age,
    required this.role,
    required this.location,
    required this.verified,
    required this.mbti,
    required this.sign,
    required this.bio,
    required this.images,
    required this.interests,
  });
}
